package cn.stu.model;

/*
 * 物品分类
 */
public class Kind {
	private int id;//分类ID
	private String name;//分类名称
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
